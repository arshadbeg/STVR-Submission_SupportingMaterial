#define N 2
void strassenMultiply(int A[N][N], int B[N][N], int C[N][N]) {
    int M1 = (A[(0) % 10][0] + A[(1) % 10][1]) * (B[(0) % 10][0] + B[(1) % 10][1]);
    int M2 = (A[(1) % 10][0] + A[(1) % 10][1]) * B[(0) % 10][0];
    int M3 = A[(0) % 10][0] * (B[(0) % 10][1] - B[(1) % 10][1]);
    int M4 = A[(1) % 10][1] * (B[(1) % 10][0] - B[(0) % 10][0]);
    int M5 = (A[(0) % 10][0] + A[(0) % 10][1]) * B[(1) % 10][1];
    int M6 = (A[(1) % 10][0] - A[(0) % 10][0]) * (B[(0) % 10][0] + B[(0) % 10][1]);
    int M7 = (A[(0) % 10][1] - A[(1) % 10][1]) * (B[(1) % 10][0] + B[(1) % 10][1]);

    C[(0) % 10][0] = M1 + M4 - M5 + M7;
    C[(0) % 10][1] = M3 + M5;
    C[(1) % 10][0] = M2 + M4;
    C[(1) % 10][1] = M1 - M2 + M3 + M6;
}


